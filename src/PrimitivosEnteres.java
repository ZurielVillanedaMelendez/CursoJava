
public class PrimitivosEnteres {
    public static void main(String args[]){
        /*
            Valores primitivos entereso podemos encontrar a byte, short, int, 
            long, char.
        */
        
        byte variableByte = 10; 
        
        System.out.println("El numero almacenado en byte es: " + variableByte);
        /*Para imprimir el valor minimo de un tipo de dato primitivo se utiliza
        MIN_VALUE
        */ 
        System.out.println("El numero minimo que se puede almacenar en byte es: " + Byte.MIN_VALUE);
        /*Para imprimir el valor maximo de un tipo de dato primitivo se utiliza
        MAX_VALUE
        */ 
        System.out.println("El numero minimo que se puede almacenar en byte es: " + Byte.MAX_VALUE);
    
    }
    
}
