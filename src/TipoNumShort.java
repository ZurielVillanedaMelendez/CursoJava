
public class TipoNumShort {
    public static void main(String args[]){
        
        short numeroShort = 10;
        System.out.println("numeroShort = " + numeroShort);
        System.out.println("El numero maximo de short es " + Short.MAX_VALUE);
        System.out.println("El numero minimo de short es " + Short.MIN_VALUE); 
        
        int numeroInt = 100;
        System.out.println("numeroInt = " + numeroInt);
        System.out.println("El numero maximo de int es " + Integer.MAX_VALUE);
        System.out.println("El numero minimo de int es " + Integer.MIN_VALUE);
        
        long numeroLong = 1000;
        System.out.println("Numero long es: " + numeroLong);
        System.out.println("El numero maximo de un tipo long es: " + Long.MAX_VALUE);
        System.out.println("El numero minimo de un tipo long es: " + Long.MIN_VALUE);
        
    
    }
    
}
